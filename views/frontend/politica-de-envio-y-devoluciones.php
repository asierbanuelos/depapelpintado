          <h2 class="titulo-1">1. Gastos de envío</h2>
          <p>
            Los precios de los productos ofertados en el sitio web no incluyen los gastos de envío.
          </p>
          <p>
            Los gastos pueden variar en función de su lugar de residencia y de las ofertas y promociones que pudieran existir en cada momento. En todo caso, serán de aplicación los siguientes:
          </p>
          <p>
            Los productos cuyas entregas requieran de plazos distintos reflejarán dicha particularidad en su descripción.
          </p>
          <p>
            En los envíos que se realicen a regiones no pertenecientes a la Unión Aduanera Europea, corresponderá al receptor de la mercancía abonar los aranceles de importación en destino.
          </p>
          <p>
            Todos los pedidos admiten entregas y facturaciones parciales. Los sucesivos envíos que se realicen para atender el mismo pedido pueden estar gravados con gastos de envío. De Papel Pintado le ofrecerá la posibilidad de escoger entre esperar a que todo el pedido este completo o la posibilidad de recibir entregas parciales.
          </p>

          <h2 class="titulo-1">2. Plazos de entrega</h2>
          <p>
            El plazo de entrega de cada producto puede encontrarse tanto en su página correspondiente como en la confirmación de pedido.
          </p>
          <p>
            En caso de no ser posible entregarle el producto adquirido, De Papel Pintado le ofrecerá la posibilidad de rescindir la compra o adquirir un producto de similares características.
          </p>
          <p>
            Si al momento de la entrega, detectase alguna disconformidad con el producto entregado, deberá indicarlo por escrito en el albarán y ponerse en contacto con De Papel Pintado de forma inmediata a través de la dirección <a href='mailto:info@depapelpintado.es'>info@depapelpintado.es</a>.
          </p>
          <p>
            De Papel Pintado no se hará responsable de los errores incurridos a la hora de facilitar la dirección de envío o por la ausencia del receptor en la referida dirección. 
          </p>

          <h2 class="titulo-1">3. Devoluciones</h2>
          <p>
            Tiene derecho a desistir de la compra de los productos adquiridos por la web hasta 14 días después de la recepción de la mercancía sin necesidad de indicar el motivo salvo en los casos que el derecho de desistimiento no sea aplicable indicados en la parte inferior de esta sección.
          </p>
          <p>
            Los pasos a seguir para realizar la devolución son los siguientes: 
          </p>
          <ul>
            <li><p>Para garantizar el proceso de devoluciones, todos los formularios de devoluciones deberán venir acompañados de fotografías del producto a devolver. Mínimo 4 fotos de cómo se encuentra el producto. En el caso de rollos de papel pintado, una foto de cada punta de los rollos, otra foto de los rollos enteros y la última como va empaquetado para ser enviado. ¡Muy importante! Que los rollos conserven los plásticos de fábrica y no estén rotos o abiertos.</p></li>
            <li><p>Deberá rellenar el formulario que puede <a href='/assets/uploads/files/formulario-de-desestimiento.pdf' target="_blank">descargar en el siguiente enlace</a> y dirigirlo a De Papel Pintado a la siguiente dirección de correo electrónico, <a href='mailto:info@depapelpintado.es'>info@depapelpintado.es</a> expresando su voluntad de desistir del producto adquirido en De Papel Pintado.</p></li>
            <li><p>En el caso de que el cliente no haya usado nuestro servicio de muestras para comprobar textura y color, la devolución será abonada con el 80% del valor total del pedido, el coste de los portes devolución será a cargo del cliente, y en los casos en que el producto sea importado fuera de la UE se devolverá el 50% en concepto de gastos añadidos por transporte y administración.</p></li>
            <li>
              <p>La mercancía deberá ser enviada por el cliente a la siguiente dirección:</p>
              <p>
                Alameda Doctor Areilza 21<br />48011 Bilbao
              </p>
            </li>
          </ul>
          <p>
            Salvo en los supuestos de entrega de bienes dañados o defectuosos, le corresponderá soportar el coste de devolución de los productos entregados. Las devoluciones deberán dirigirse a la dirección que le indique De Papel Pintado. 
          </p>

          <p class='titulo-2-bold'>El derecho de desistimiento no será aplicable en los siguientes supuestos:</p>
          <ul>
            <li><p>Aquellos productos que, por haber sido abiertos, usados y/o que no conserven su precinto o embalaje original puedan deteriorarse o caducar con rapidez.</p></li>
            <li><p>Producto estándar o retail: Esta categoría de producto puede estar disponible en stock o bajo pedido. Aquellos productos que no se encuentren en stock al momento de realizar la compra podrán ser pedidos a través de la plataforma y serán fabricados bajo encargo. Es importante que conozcas que cuando adquieres productos estándar o retail fuera de stock y hayas realizado un encargo para la fabricación de los mismos, reconoces y asumes que durante el periodo de fabricación y entrega de este tipo de productos no podrás ejercer tu derecho de desistimiento, así como tampoco tras la entrega de los mismos.</p></li>
            <li><p>Productos a medida: Esta categoría de productos se fabrica conforme a las especificaciones del cliente o personalizados (por ejemplo, telas a medida, papeles pintados, murales, etc).</p></li>
            <li><p>Aquellos productos que se devuelvan incompletos o formen parte de un lote. Este supuesto será aplicable a aquellos supuestos en los que el producto que se pretenda devolver esté incompleto por no incluir todos sus accesorios o formar parte de un lote. Usted sólo es responsable de la depreciación de los bienes resultante de una manipulación distinta a la necesaria para establecer la naturaleza, las características y el buen funcionamiento de los bienes.</p></li>
          </ul>
          <p>
            Es muy importante que tenga en cuenta estas instrucciones, ya que cualquier devolución que no se realice atendiendo los trámites previstos en la presente clausula no podrá ser tramitada y, en consecuencia, será rechazada.
          </p>
          <p>
            Las cantidades satisfechas le serán devueltas en plazo máximo de 14 días naturales desde que De Papel Pintado reciba los productos devueltos, y alternativamente desde que haya presentado una prueba de la devolución de los bienes, según qué condición se cumpla primero.
          </p>

          <h2 class="titulo-1">4. Envío standard: Entrega a pie de calle</h2>
          <p>
            Con el servicio de envío estándar la entrega se realiza en el portal de su casa o edificio. La agencia de paquetería no está obligada a realizar la entrega en el interior de su vivienda. En ocasiones, el repartidor puede optar por ayudar voluntariamente en la subida de la mercancía, pero esto queda a su discreción y no es responsabilidad de De Papel Pintado.
          </p>

          <h2 class="titulo-1">5. Envío Especial: Subida a Domicilio</h2>
          <p>
            Con este servicio la empresa de transporte realizara la entrega y subida al portal de su casa o a la estancia que se designe. Para contratarlo, contáctanos vía correo electrónico a <a href='mailto:info@depapelpintado.es'>info@depapelpintado.es</a>  o telefónicamente indicando el número de pedido y solicitando el servicio de "Subida a Domicilio".
          </p>
          <p>
            Si su pedido incluye artículos que requieren entrega por un equipo de al menos dos personas, se aplicará una tarifa adicional. Ten en cuenta que esta tarifa solo aplica a productos que necesitan entrega por un equipo de dos o más personas debido a su tamaño o peso.
          </p>
          <p>
            Es fundamental que el cliente garantice la accesibilidad del producto a su domicilio, verificando previamente la viabilidad de su transporte a través del ascensor o escaleras. En caso de restricciones que impidan el acceso, lamentablemente no podremos realizar la subida del producto. Es responsabilidad del cliente asegurarse de que las condiciones sean adecuadas para la entrega del producto en su ubicación deseada. No se ofrece reembolso de este servicio por restricciones de acceso no garantizadas por el cliente.
          </p>
          <p>
            El costo de la subida a domicilio varía según el volumen y peso de los productos. Ponte en contacto con nosotros para obtener información detallada y tarifas correspondientes. No se ofrece reembolso por este servicio en caso de devolución del producto.
          </p>
