          <h3 class='titulo-1-grande' id="tiposdepapel">Tipos de papeles pintados</h3>
          <h4 class='titulo-2-bold'>Papeles pintados de papel</h4>
          <p>Se componen de dos capas de papel cuya capa superior está impresa.</p>
          <h4 class='titulo-2-bold'>Papeles pintados con relieve</h4>
          <p>Se componen de una capa de papel con impresión de pasta en pintura expandida tridimensional, casi siempre en varios colores. Son especialmente fáciles de manejar y también son apropiados para paredes sometidas a grandes esfuerzos y para espacios húmedos.</p>
          <h4 class='titulo-2-bold'>Papeles pintados vinílicos</h4>
          <p>Se componen de un papel revestido que se estampa en caliente una vez finalizada la impresión. Así se obtiene una superficie especialmente atractiva.<br>
          Además, también son apropiados para paredes sometidas a grandes esfuerzos o espacios húmedos.</p>
          <h4 class='titulo-2-bold'>Papeles pintados <em>non woven</em> (no tejidos)</h4>
          <p>A diferencia de los papeles pintados a base de un soporte de papel, los papeles pintados <em>non woven</em> comprenden un tejido <em>non woven</em> especial de alta calidad que se emplea como material de soporte. Los papeles pintados non woven se ofrecen con superficie en relieve o vinílica. Estos papeles pintados resultan especialmente fáciles de aplicar. En el empapelado, se encola el techo o la pared para luego proceder a aplicar la tira de papel pintado sobre la base recién encolada: ya no es necesaria una mesa para encolar el papel. Las tiras de papel pintado son dimensionalmente estables (es decir, no se produce ni dilatación ni contracción), por lo que ya no es necesario el tiempo de reposo. Además, es posible cubrir pequeñas grietas. En caso de renovación del papel pintado, este producto se puede desprender en seco de la pared facilmente, sin dejar restos.</p>

