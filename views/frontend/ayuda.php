          <h3 class='titulo-1-grande mt-4'  id="obligaciondecomprobacion">Verificaciones obligadas antes de la aplicación</h3>
          <p>La Hoja informativa nº 7 de la Comisión Federal del Color y la Protección del Valor Real, BFS. Las directivas de prueba para revestimientos de pared antes de la aplicación, describen las siguientes medidas de prueba a realizar por el usuario antes de la aplicación:</p>
          <ul>
             <li>Control del suministro, es decir ¿coincide el suministro con la mercancía
               pedida y se suministró la cantidad pedida?</li>
             <li>Antes de retirar el embalaje transparente hay que controlar el número de
               tintada. Todos los rollos previstos para un espacio o para una pared deberían
               proceder de la misma fabricación.</li>
             <li>En el control de la <strong>igualdad de lados</strong> (prueba del abanico), los dos<br>
               extremos de una pieza de muestra, de aprox. 1,00 m, se pliegan hacia el centro
               con el estampado hacia arriba. De esta forma se detectan la igualdad de lados,
               así como las tolerancias de color, en la mayoría de los casos.</li>
             <li>Desenrollar aprox. 1,5 m de tres rollos colocados uno al lado del otro y
               controlar la igualdad de color.</li>
             <li>Durante el corte de las tiras y durante el empapelado se controlará la
               ausencia de fallos del papel pinado o del dibujo.</li>
             <li>Los restos y la hoja de información se deben guardar.</li>
          </ul>
