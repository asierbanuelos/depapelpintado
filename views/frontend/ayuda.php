          <h4 class='titulo-2-bold'  id="preparaciondelabase">Preparación de la base</h3>
           <p>Una preparación correcta de la base es muy importante para un resultado óptimo.</p>
           <p>Por norma general, la base debe estar seca, lisa, firme, limpia, resistente y absorbente. Si se cumplen estas reglas básicas, el proceso de empapelar se desarrollará sin problemas. ¿Pero qué significa esto exactamente? En primer lugar quisiéramos acabar con un prejuicio que sigue estando ampliamente divulgado: Es falsa la afirmación de que en las construcciones nuevas no se puede emplear papel pintado sino solamente sucedáneos debido a la humedad restante. Porque si la base está correctamente preparada, cualquier papel pintado se puede aplicar sin problemas, incluso en construcciones nuevas. </p>
           <p>El papel pintado antiguo debería quitarse previamente por completo. Esto se lleva a cabo rápida y sencillamente con un papel pintado separable o desprendible en seco.</p> 
           <h4 class='titulo-2-bold'>Base</h4>
           <p>Por lo demás, los papeles pintados viejos deberían mojarse con agua y con un
             líquido removedor. Una vez que el papel pintado se ha empapado, resulta más
             fácil quitarlo. También resulta fácil quitar rápidamente papeles viejos con un
             dispositivo separador por vapor caliente (los comercios especializados en
             papeles pintados los alquilan). Para quitar el papel pintado no se debería
             emplear detergente lavavajillas. Estos productos son disolventes de grasa y
             pueden disolver sustancias como grasas (en cocinas) así como otros residuos
             de la base. Con la aplicación de la cola que contiene agua sobre la base
             durante el empapelado, estos residuos pueden ser activados y transportados a
             la superficie. Estos residuos, en parte muy agresivos, pueden penetrar en el
             papel pintado y desteñir la superficie. En caso de bases con sustancias
             penetrantes es necesario aplicar una capa de pintura aislante.</p>
             <p>Eliminar las capas de pinturas antiguas cretáceas empapándolas con agua
               mezclada con líquido removedor. Tras el secado, la base debería sellarse con
               una capa de imprimación. </p>
             <p>Con respecto al resto de pinturas antiguas (como pinturas de dispersión, al óleo
             y barnices), primero se debe controlar si están firmemente unidas con la base.
             Emplear, a tal efecto, una cinta adhesiva que se pega sobre la pintura antigua
             para volver a quitarla. Si hay residuos en la cara posterior de la cinta adhesiva, hay que eliminar la pintura antigua. En otros casos, pintar previamente la capa
             de pintura antigua o tratarla con un disolvente adecuado y lavar posteriormente
             con agua (de lo contario, no se adhiere la cola); también existe el peligro de
             que la humedad de la cola no pueda penetrar en la base. Eliminar las bases
             antiguas que ya no estén firmes (acabados de construcciones antiguas, etc.) y
             tratar la pared previamente con una imprimación profunda para empapelar. Si
             procede, emplear papel maculatura o una base <em>non woven</em>. Para emplastecer
             agujeros y rugosidades se debería emplear de forma exclusiva emplaste de
             yeso. </p>
           <p>Es válido para acabados nuevos: evidentemente, la base debe ser clara y
             seca. Por regla general, esta situación se da al cabo de seis semanas. En caso
             de dudas, cubrir una superficie de aprox. 50 x 50 cm con film plástico y cinta
             adhesiva durante unas 24 horas. Si al cabo de este tiempo se ha formado
             humedad debajo del film, el espacio se debería ventilar o calentar durante un
             tiempo. Dado que un acabado nuevo es una base muy absorbente, antes de
             empapelar debería someterse a un tratamiento previo con una cola buena. Las
             superficies lisas de hormigón (techos, etc.) deberían imprimarse antes de
             empapelar (encolado previo, base de papel pintado pigmentado, imprimación
             profunda para empapelar).   </p>
           <p>En caso de paredes y techos que hayan sido emplastecidos en toda su
             superficie con un emplaste, es recomendable y casi siempre suficiente un
             encolado previo después de esmerilizar la superficie para igualarla. Con ello se
             obtiene un poder absorbente uniforme de la base. Además, en este tipo de
             imprimación se disuelve ligeramente la capa de cola en el posterior
             empapelado con lo que aumenta el poder adhesivo de la cola
             correspondientemente aplicada.</p>
             
           <h4 class='titulo-2-bold'>Resumen</h4>
             <p><strong>Acabado nuevo; Acabado de cal y cemento</strong></p>
             <ul>
               <li>Prueba de humedad con agua (test de poder absorbente)</li>
               <li>Si procede, lijar y rectificar (para eliminar capas sinterizadas = capa de
                 separación en la base, enriquecimiento del aglomerante)</li>
               <li>Imprimación con una capa de pintura base pigmentada<br>
               </li>
             </ul>
             <p><strong>Acabado de yeso</strong></p>
             <ul>
               <li>Prueba de humedad con agua (test de poder absorbente)</li>
               <li>Si procede, lijar y rectificar (para eliminar capas sinterizadas = capa de
                 separación en la base, enriquecimiento del aglomerante)</li>
               <li>Imprimación: Encolado previo</li>
              </ul>
             <p><strong>Cartón enyesado</strong></p>
             <ul>
               <li>En papeles pintados <em>non woven</em> imprimación con pintura base pigmentada
                 (imprescindible porque, de lo contrario, pueden ser visibles las costuras de
                 emplaste blancas a través del papel tapiz <em>non woven</em> seco)</li>
               <li>O imprimación con imprimación profunda para papel pintado(diluida con
                 agua) (p. ej. en papeles pintados vinílicos y con relieve)    </li>
             </ul>
             <p><strong>Superficies de hormigón</strong></p>
             <ul>
               <li>Prueba de humedad con agua (test de poder absorbente)
               </li>
               <li>Si procede, lijar y rectificar (para eliminar capas sinterizadas = capa de
                separación en la base, enriquecimiento del aglomerante)</li>
               <li>Imprimación con una capa de pintura base pigmentada.</li>
               <li>En caso de un poder absorbente insuficiente, emplear papel maculatura o<br>
                 una base <em>non woven</em>.</li>
              </ul>
             <p><strong>Bases de madera</strong>
                </p>
             <ul>
               <li>En materiales de madera (tablas de virutas, tablas MDF, etc.), puede
                 producirse durante el empapelado una impregnación del papel pintado con los
                componentes de la madera.</li>
               <li>Si procede, aplicar una pintura de aislamiento o pegar una base <em>non woven</em>.
               </li>
              </ul>
             <p><strong>Bases de estiropor</strong></p>
             <p>Se trata de una base poco absorbente. Existen modelos previamente
                imprimados, pero también brutos. A este respecto, para una adhesión segura
                del papel pintado es recomendable una imprimación con un pegamento para
                estiropor diluido como puente de adhesión o, en su caso, la aplicación de papel
                maculatura o de una base <em>non woven</em>.</p>

             <h3 class='titulo-1-grande mt-4' id="lacolaadecuada">La cola adecuada</h3>
             <h4 class='titulo-2-bold'>Para papeles pintados <em>non woven</em>, cola para materiales non woven</h4>
             <p><strong>Ventajas de la cola para papeles pintados <em>non woven</em>:</strong></p>
             <ul>
               <li>Permanece durante largo tiempo en la pared, es decir, permanece al
                 descubierto durante mucho tiempo (importante para el pegado a la pared)</li>
               <li>Casi no salpica</li>
               <li>Garantiza la desprendibilidad en seco en caso del correspondiente
                 Para papeles pintados vinílicos y con relieve; Cola especial
                 más un plus del 20%-30% de cola de dispersión (cola vinílica preparada).     </li>
               <li>La parte de cola de dispersión hace que en papeles pintados separables el
                 papel base se adhiera firmemente a la base y que en trabajos posteriores
                 pueda servir como papel maculatura (si procede, realizar antes una prueba de
                 uso).</li>
             </ul>
             <p><strong>Para papeles pintados de papel; Cola normal o especial     </strong></p>
             <ul>
               <li>Una cola normal no mancha tanto porque no contiene partes de dispersión.
                 Sin embargo, la cola especial es recomendable porque es sencillo de trabajar
                 con ella.</li>
             </ul>
             <p><strong>Para papeles pintados non woven de renovación; Cola para acabados
              nuevos (recomendable)</strong></p>
             <ul>
               <li>Se puede emplear cola de dispersión pura si el papel pintado <em>non woven</em> de
                 renovación permanecerá en posteriores trabajos de renovación en la pared y si
                debe servir de base en posteriores trabajos.</li>
               <li>Otra variante es el empleo de una cola para estiropor, p. ej. en el retoque de
                 tejidos de vidrio con varias capas de pintura. En este caso, la cola se aplica con
                 una espátula dentada sobre el tejido de vidrio. El papel pintado<em> non woven </em>de
                 renovación se coloca en seco y se alisa con una paleta o una espátula para
                empapelar.</li>
             </ul>
             <p><strong>¡Importante! </strong>Para el posterior empapelado debería aplicarse una fina capa de
               cola sobre el papel pintado non woven para regular la penetración de la cola en
              el fondo.</p>
             <p>Ejemplo: En el procedimiento “papel pintado <em>non woven</em> sobre papel pintado
               <em>non woven</em>”, una determinada parte de la cola penetra en el papel pintado non<br>
               woven de renovación y otra parte de la cola en la cara posterior del papel
               pintado non woven. Esto puede dar lugar a problemas de adhesión.</p>
            
             <h3 class='titulo-1-grande mt-4' id="cortaraplicarcola">Empapelado</h3>
             <h4 class='titulo-2-bold'>Corte</h4>
             <p>En primer lugar, compruebe si los rollos de papel pintado presentan el mismo
               número de artículo y de lote, y si los dibujos y el color son iguales. Una vez
               efectuadas dichas comprobaciones, puede comenzar. La longitud de tira
               correcta es la altura del espacio más un exceso de 5 a 10 cm. En caso de
               dibujos que no necesiten ser alineados, todas las tiras pueden cortarse
               sucesivamente del rollo. En caso de una alineación recta, todas las tiras son
               idénticas y para cortarlas los dibujos se ponen uno encima de otro. En caso de
               dibujos que se deban colocar de forma desplazada, el dibujo se desplaza en
               cada segunda tira en la mitad del cuadro de dibujo buscando la continuidad del
               mismo, siendo idéntica cada segunda tira. Es importante que el papel tapiz se
               desenrolle solamente en una dirección.     </p>
             <h4 class='titulo-2-bold'>Encolado     </h4>
             <p>Con respecto al tipo de cola adecuada, consulte la hoja informativa del rollo.</p>
             <p>Por norma general, siempre hay que tener en cuenta que la cola debe removerse con cuidado y dejarla reposar de manera suficiente. Encolar uniformemente las tiras de papel pintado, doblar dos tercios desde arriba y un tercio desde abajo; a continuación, enrollar. Prestar atención a que se cumpla el tiempo de reposo para todas las tiras, tal y como se indica en las instrucciones del rollo. Esto es importante para la adhesión impecable del papel pintado a la pared.</p>
             <h4 class='titulo-2-bold'>Instalación<a id="instalacionempapelado"></a></h4>
             <p>Siempre se empieza “con la luz”, es decir, en la ventana. La primera tira se alinea verticalmente, con el nivel de burbuja o con la plomada, para que las siguientes tiras discurran rectas. La tira de papel pintado debería colocarse con algo de sobrante en el techo para poder compensar eventuales diferencias de altura. Presionar las tiras con un rodillo de goma esponjosa desde el centro hacia los lados, a fin de eliminar burbujas. Si procede, encolar previamente la zona de la unión de las tiras. Quitar las manchas de cola inmediatamente con una esponja o un paño húmedo; no frotar. Durante el secado, evitar la presencia de calefacción excesiva o de corrientes de aire porque, de lo contrario, pueden abrirse las uniones.</p>
             <p>Las tiras de papel pintado se colocan justo al borde de la zona de unión. Para alisar la unión se recomienda emplear un rodillo cónico o engomado. En papeles pintados con relieve no se debe emplear un rodillo porque se alisaría el relieve. En este caso, presionar la zona de unión ligeramente con un paño suave.</p>
             <p>Las salidas de cola en la superficie del papel pintado se deben quitar de forma inmediata y por completo, empapando con una esponja húmeda para secarlas posteriormente tocándolas ligeramente con un paño seco. Si la cola se elimina pasado un rato, puede dar lugar a que la superficie del papel pintado se destiña. Además, los residuos de cola no eliminados pueden dejar zonas brillantes que resulten visibles al mirar desde los lados.</p>
             <h4 class='titulo-2-bold'>Esquinas<a id="esquinaspuertashuecos"></a></h4>
             <p>Una tira grande nunca debería colocarse en la esquina. El sobrante tan sólo
               debería ser de 0,5 cm, aproximadamente. El resto de la tira se vuelve a alinear
               y a colocar en la pared nueva. Si procede, aplicar previamente una capa de
               cola de dispersión en la zona de esquina para garantizar una mayor adherencia
               a la misma.</p>
             <h4 class='titulo-2-bold'>Puertas</h4>
             <p>Por norma general, el papel pintado se coloca siempre desde un lado hacia la
               puerta, nunca desde dos lados. La tira de papel se divide a la altura del borde
               superior horizontal del marco de la puerta si el dibujo lo permite. El papel
               pintado se presiona a lo largo del borde exterior vertical del marco de la puerta
               cortándose el sobrante. Si procede, se inserta una tira según el curso del dibujo
               con un corte doble. El sobrante por encima del marco de la puerta se adapta y<br>
               se corta.    </p>
             <h4 class='titulo-2-bold'>Huecos de ventana</h4>
             <p>Se coloca en un lado de la ventana una tira de papel pintado aplomada de tal forma que el sobrante “alrededor de la esquina” alcance el marco de la ventana (más un exceso). Entonces se realiza un corte en el papel tapiz, a la altura de la repisa y del borde superior de la ventana, de forma que el sobrante se pueda doblar dentro del hueco. Los sobrantes en el marco de la ventana y en la repisa se cortan. A continuación, empapelar por encima y por debajo de la ventana y proceder de la misma manera en el otro lado de la ventana.</p>
             <h4 class='titulo-2-bold'>Radiadores<a id="radiadoresinterruptorescajas"></a></h4>
             <p>Se recomienda colocar el papel pintado por todos los lados como máximo
               hasta 10 a 20 cm detrás del radiador.</p>
             <p>Interruptores de luz y cajas de enchufe
               Antes de trabajar en cajas de enchufe e interruptores de luz, desconecte la corriente eléctrica en la caja de fusibles. En todo momento debería emplearse
               un destornillador aislado, preferiblemente un comprobador de fases que
               permita controlar de forma segura si la corriente realmente se ha
               desconectado. Primero se quitan las cubiertas de interruptores y cajas de
               enchufes para que sea posible empapelar “por debajo” de las mismas. La tira
               de papel pintado se coloca simplemente por encima de la caja de enchufe o del
               interruptor de luz (sin tener en cuenta la posición del mismo). En las cajas de
               enchufe, los contactos de puesta a tierra se empujan a mano a través del papel
               pintado. En los interruptores de luz se realiza un corte en cruz en el papel
               pintado. Una vez que la cola se haya secado, se corta con el cuter la zona del
               interruptor o del elemento de caja de enchufe de forma que éstos últimos
               queden al descubierto. La zona descubierta no debería ser más grande que el
               marco de metal para que la cubierta tape el recorte por completo. Al final, se
              vuelven a colocar las cubiertas.</p>
             <h4 class='titulo-2-bold'>Techo<a id="techocenefas"></a></h4>
             <p>Después de realizar un marcado (se recomienda un trazado de cordón) de las
               primeras tiras de papel pintado, el papel pintado se coloca con la luz
               alejándose de la ventana. El tapizado del techo es más fácil cuando se realiza
               entre dos personas: Mientras que una persona coloca y presiona la tira de
               papel contra el techo, la segunda sujeta la tira restante aún doblada con ayuda
               de una escoba.</p>
             <h4 class='titulo-2-bold'>Cenefas</h4>
             <p>En la aplicación de cenefas se diferencian dos tipos. Para papeles pintados
               lisos, la posición de la cenefa se marca previamente para luego pegarla en el
               papel pintado; humedecer ligeramente la cara posterior de la cenefa con agua
               mediante una esponja, aplicar el pegamento para cenefas y colocar la cenefa
               una vez transcurrido un tiempo de reposo de unos 2 a 5 minutos. En papeles
               pintados muy estructurados, es recomendable colocar la cenefa directamente
               en la pared para luego colocar las tiras de papel pintado de arriba a abajo a lo
               largo de la cinta. Para obtener uniones limpias, los excedentes sencillamente
               se cortan con la recanteadora.</p>
             <h4 class='titulo-2-bold'>Técnica de pegado a la pared para papeles pintados
               <em>non woven</em><a id="dispositivosparaencolar"></a></h4>
             <p>Los papeles pintados <em><strong>non woven</strong></em> de Rasch son de muy fácil y rápida aplicación:
               para la aplicación, recomendamos el empleo de cola para papeles pintados non
               woven. Remover con cuidado la cola y dejar que repose suficientemente. Se
               suprimen operaciones como encolar y doblar el papel pintado, así como los
               tiempos de reposo.</p>
             <p>La cola se aplica regular y suficientemente con un rodillo especial, directamente
               en la pared o el techo. A continuación, la tira seca y cortada (dibujo en el lado
               interior) se coloca sobre la base encolada de la pared o del techo (los papeles
               pintados non woven no se dilatan ni se contraen). Después, se procede al
               empapelado de esquinas, terminaciones, calefacción, etc., tal y como se ha
               descrito anteriormente.</p>
             <p>¡No es posible crear una nueva vivienda de forma más rápida y sencilla que
               con papel pintado <em>non woven</em>!</p>
             <p>Dispositivo para encolar papeles pintados <em>non woven</em>. Los papeles pintados <em>non woven</em> también se pueden tratar con un dispositivo
          para encolar, Siempre debería pasarse una única tira de papel a través del
          dispositivo para aplicarla inmediatamente.</p>
             <h3 class='titulo-1-grande mt-4'>Particularidades<a id="particularidadesinstalacion"></a></h3>
             <p>Particularidades en la aplicación de bases non woven
          repintables para el diseño individual de la pared, p. ej.
          WALLTON de rasch.</p>
             <p>Se trata de un semi-producto que se embellece mediante técnicas de pintura,
               laca o técnicas creativas.
               En el recubrimiento con pinturas de dispersión se deben tener en cuenta los
               siguientes puntos: Las posibles salidas de cola durante el empapelado se
               deben eliminar de forma inmediata. De lo contrario, puede dar lugar a un
               comportamiento absorbente irregular así como a manchas después de pintar
               (la cola se seca y bloquea en esta zona el papel pintado a recubrir).     </p>
             <p>Elegir una pintura de dispersión resistente a los plastificantes porque, de lo
               contrario, puede dar lugar a una interacción (efecto de adhesión) entre el papel
               pintado y el recubrimiento.
               La aplicación de la pintura debería efectuarse con un rodillo de microfibras
               cortas, de forma regular y “húmedo en húmedo”. Procurar que la consistencia
               de la pintura se ajuste de forma correcta.</p>
             <p>Ocasionalmente, se hace necesaria
               una segunda capa de pintura.</p>
             <p>Los sombreados que puedan aparecer debido a estructuras de tela, estructuras
               de tejido, efectos naturales, etc., en la pared, son intencionados y subrayan el
               carácter de la misma.</p>
             <p>Estos efectos que aparecen intencionadamente no se pueden eliminar o
               compensar por la aplicación de una o varias capas de pintura. Sobre todo en la
               zona del techo, en condiciones de luz especialmente críticas, puede ser
               necesario poner en práctica el procedimiento de pintura en cruz.</p>
             <p>Las técnicas creativas de cualquier tipo (técnicas de lacado, esponjado,
               trapeado) deberían probarse previamente en piezas de muestra de papel
               pintado para ver si se consigue un resultado satisfactorio. </p>
             <h3 class='titulo-1-grande mt-4'>Tratamiento de problemas: Problemas con la base<a id="problemasbase"></a></h3>
             
             <p>La causa más frecuente de problemas en el empapelado es la preparación, a menudo deficiente, de la base. Por desgracia, al observador externo le resulta
               difícil interpretar estos problemas una vez finalizado el trabajo.     </p>
             <p><strong>Base:</strong> En caso de formación de burbujas o arrugas, uniones contraídas o si el
               papel pintado “no se sujeta”, esto puede deberse a varias razones: </p>
             <p><strong>Capas sinterizadas</strong>: En este caso, la base no es lo suficientemente
               absorbente. Causa: La base está recubierta por una capa sinterizada. Las
               capas sinterizadas son acumulaciones de aglomerantes en la superficie que se
               pueden formar al compactar y alisar el acabado de cal y cemento pero también
               en acabados de yeso. Estas capas sinterizadas se depositan como una
               película en la superficie y bloquean la base.
               Las capas sinterizadas se pueden detectar con luz lateral por zonas brillantes
               en la superficie. La capa sinterizada debe eliminarse lijando.</p>
             <p> <strong>“Encolado excesivo de la base” </strong>en superficies varias veces empapeladas:
               Si las superficies a empapelar ya fueron empapeladas en repetidas ocasiones,
               puede haber varias capas de residuos de cola en la superficie. Tampoco aquí
               queda ya garantizado un poder absorbente óptimo porque los residuos
               recubren y bloquean la base. En este caso, la base debería lijarse con papel
               abrasivo para aplicar una capa de imprimación reguladora sobre la base (p. ej.
               una base de papel pintado pigmentado, papel maculatura o una base non
               woven).</p>
             <p>Importante: La base debe ser lo suficientemente absorbente como para que la
               cola pueda anclarse en la misma con el fin de garantizar una adhesión inicial
               suficiente.</p>
             <p><strong>Imprimaciones de maculatura antiguas</strong>, que se encuentran sobre la base,
               pueden llegar a disolverse y dar lugar a fallos. La maculatura está pasada de
               moda y no resulta apropiada para el empapelado (con excepción del papel
               basto de fibras).</p>
             <p><strong>Imprimación profunda LF e imprimación profunda de papel pintado:</strong> La imprimación profunda LF (que a menudo se aplica sin diluir) bloquea
               demasiado la base para trabajos de empapelado (frecuentemente la aplicación
               con la brocha o el rodillo de techo da lugar a superposiciones de la
               imprimación). Por este motivo la cola no puede penetrar de forma óptima en la
               base y no se une lo suficientemente con ella (adhesión inicial). No queda
               garantizada una adhesión inicial suficiente, lo que puede dar lugar a la apertura
               de uniones e, incluso, a la formación de burbujas. Para empapelar siempre
               debería emplearse una imprimación profunda de papel pintado ya que ésta, a
               diferencia de la imprimación profunda LF, contiene partículas más pequeñas
               que penetran más profundamente en la base (efecto capilar). La imprimación
               profunda de papel pintado no se deposita en la base para bloquearla sino que
              mantiene el poder absorbente de la misma. </p>
             <p>¡Una base suficientemente
               absorbente tiene una importancia decisiva para conseguir un buen
               empapelado! </p>
             <h4>Pintura de dispersión     </h4>
             <p>La pintura de dispersión es altamente resistente al lavado y bloquea demasiado
               la base. Tampoco en este caso puede la cola aplicada penetrar de forma
               óptima en la base para desarrollar una adhesión inicial suficiente. Esto provoca
               a menudo una formación de burbujas extrema (también en caso de papel
               pintado non woven). Incluso al cabo de varias semanas (especialmente en
               caso de fluctuaciones de temperatura) todavía podrían producirse aperturas de
               uniones.
               La alternativa recomendada es la imprimación de papel pintado pigmentada
               que regula la base de forma óptima para trabajos de empapelado (poder
               absorbente).     </p>
             <h3 class='titulo-1-grande mt-4'>Problemas con la cola y tiempos de reposo<a id="problemascolatiempo"></a></h3>
             <p>Si la cola se diluye demasiado pierde hasta más del 50% de su poder adhesivo.
               El resultado es una adhesión deficiente a la base: El papel pintado no se pega
               correctamente a la base, las zonas de unión se levantan o se abren y pueden
               formarse burbujas (en parte incluso al cabo de varias semanas, especialmente
               durante la época de calefacción cuando entre la pared y el papel pintado se
               forma una tensión superficial, con una temperatura exterior de 10 grados bajo
               cero e interior de 22 grados). Debe observarse el tiempo de reposo indicado del
               papel pintado. Se prestará atención a que todas las tiras tengan el mismo
               reposo (unos 10 minutos, aproximadamente).
               
               Consejo: En caso de una temperatura ambiente extremamente alta (verano),
               las tiras de papel pintado se guardan en una bolsa de plástico para evitar un
               secado prematuro.</p>
             <h4 class='titulo-2-bold'>Papel pintado de papel    </h4>
             <p>En los papeles pintados de papel, la observación del tiempo de reposo es muy
               importante. Cualquier tiempo de reposo inferior al estipulado conduce
               forzosamente a contracciones y a formación de burbujas y arrugas. En el papel
               pintado con relieve es importante que la cola también penetre en las zonas más
               profundas del relieve. De lo contrario, se formarán diferentes tensiones de
               secado.
               Papeles pintados con relieve y vinílicos
               
               En el papel pintado vinílico también resulta imprescindible la observación del
               tiempo de reposo. El papel vinílico siempre se debe enrollar después del
               encolado para evitar así un secado de las zonas de los bordes. También aquí
               hay que procurar que la base sea lo suficientemente absorbente porque el
               papel pintado se seca de forma casi exclusiva hacia la base.     </p>
             <h4 class='titulo-2-bold'>Papeles pintados non woven     </h4>
             <p>En los papeles pintados non woven resulta especialmente necesaria una base
               lo suficientemente absorbente y de color neutral. Si la base no es uniforme
               desde el punto de vista del color, este dibujo de la base puede resultar visible a
               través del papel pintado. Por lo tanto, se recomienda la aplicación de una
               imprimación de papel pintado pigmentada.
               Al empapelar sobre una base non woven, se coloca material non woven sobre
               material non woven penetrando parte de la cola en la cara posterior non woven
               del papel pintado y otra parte en la base non woven, lo que puede resultar en
               una adhesión deficiente del papel pintado a la base non woven. En este caso,
               se recomienda un encolado previo de la base non woven.     </p>
             <h3 class='titulo-1-grande mt-4'>Daños: Posibles daños y su causa <a id="danios"></a></h3>
             <h4 class='titulo-2-bold'>Diferencias de color</h4>
             <p> ¿Presentan todos los rollos aplicados el mismo número de lote? Si fuera así, el
               fallo debería haberse detectado ya durante el control antes de la aplicación, ver
               el punto 3 y la hoja informativa nº 7 de BFS (Comisión Federal del Color y la
               Protección del Valor Real) Directivas de control para revestimientos de
               pared antes de la aplicación.</p>
             <h4 class='titulo-2-bold'>Sombreados     </h4>
             <p>Si en la pared empapelada se presentan sombreados, podría ser que el papel
               pintado se haya colocado en dirección opuesta. De lo contrario, el fallo ya
               podría haberse detectado en el control antes de la aplicación (prueba de abanico).</p>
             <h4 class='titulo-2-bold'>Formación de manchas</h4>
             <p> Las manchas grises casi siempre se forman en caso de alcalinidad de la base.</p>
             <h4 class='titulo-2-bold'>Manchas de color marrón     </h4>
             <p>Pueden ser manchas de óxido de piezas metálicas en la base (clavos,
               soportes, metal desplegado, óxido de hierro en la arena de limpieza, etc.)</p>
             <h4 class='titulo-2-bold'>Manchas de color amarillo     </h4>
             <p>Se presentan en bases de acabados en yeso y de madera. Los residuos de la
               base que han sido disueltos por la cola acuosa, como p. ej. nicotina, grasas,
               aceites, componentes de la madera y otras sustancias, penetran a través del
               papel pintado.</p>
             <h4 class='titulo-2-bold'>Manchas de color rojo o violeta-rosa     </h4>
             <p>Son un indicio de paredes húmedas y/o moho.</p>
             <h4 class='titulo-2-bold'>Problemas en las zonas de unión</h4>
             <p> Las uniones se abren o se levantan si la base no es lo suficientemente
               absorbente (ver el punto 5.1.) o si no se ha elegido la cola adecuada. También
               las corrientes de aire o la calefacción pueden originar este problema.</p>
             <h4 class='titulo-2-bold'>Áreas brillantes en la zona de unión</h4>
             <p>Si las salidas de cola no se han eliminado de forma correcta y completa pueden
               formarse zonas brillantes en la zona de unión.</p>
             <h4 class='titulo-2-bold'>Olor</h4>
             <p>Cada papel pintado presenta un determinado olor propio que desaparece al
               cabo de algunos días. Sin embargo, si el olor persiste, puede deberse a que
               sustancias y residuos de la base hayan sido disueltas durante un empapelado
               con aplicación de abundante agua (p. ej. al empapar el papel pintado antiguo
               para poder quitarlo, en trabajos con espátula, en un empapelado nuevo) y
               literalmente “apesten”.</p>
            
